    public UI(String username) {
        initComponents();
        javax.swing.Timer timer = new Timer(300, new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                try {
                    List<String> conv = client.getConversation();
                    for (String item : conv) {
                        txtConversation.setText(txtConversation.getText() + "\n" + item);
                    }
                    conv.clear();
                    client.setConversation(conv);
                } catch (Exception e) {
                }
            }
        });
        timer.start();
        this.setTitle("logged in as: " + username);
        this.username = username;
    }

